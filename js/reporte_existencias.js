function buscar_empelado1()
{
	
	$("#DivLoadEmpleado1").show();
	$("#GridBusquedaEmpleado1").empty();
	var Clave = $("#chkClaveEmpleado1:checked").val();
	 var Nombre = $("#chkDescripcionEmpleado1:checked").val();
	    
	    
	    if(Clave=="Clave")
	     {
	         Clave='Si';
	     }
	    
	   if(Nombre=="Descripcion")
	     {
		   Nombre='Si';
	     }

	      
	    var losdatos ={txtPatron:$("#txtPatronEmpleado1").val(),Clave:Clave,Nombre:Nombre,Sel:1};
	    
	    $.ajax({
	                url:'../../../scripts/oper_reporte_existensias.php?o=1',
	               type:'POST',
	               data:losdatos,
	               success:function(data)
	                {
	            	   
	            	   $("#GridBusquedaEmpleado1").append(data); 
	            	   $("#DivLoadEmpleado1").hide();
	                    
	                },
	                error:function(req,e,er) {
	                	alert('error!');
	                }
			}); 

}
